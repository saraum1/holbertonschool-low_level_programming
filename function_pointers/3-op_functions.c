#include "3-calc.h"

/**
 * op_add - returns sum
 * @a: int
 * @b: int
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference
 * @a: int
 * @b: int
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns product
 * @a: int
 * @b: int
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns division
 * @a: int
 * @b: int
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns modulo
 * @a: int
 * @b: int
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

